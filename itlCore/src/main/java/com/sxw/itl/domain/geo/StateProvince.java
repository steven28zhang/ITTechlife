public class StateProvince {

	private String staProCode = null;
	private String staProName = null;
	private String countryCode = null;

	/**
	 * @return the staProCode
	 */
	public String getStaProCode() {
		return staProCode;
	}

	/**
	 * @param staProCode
	 *            the staProCode to set
	 */
	public void setStaProCode(String staProCode) {
		this.staProCode = staProCode;
	}

	/**
	 * @return the staProName
	 */
	public String getStaProName() {
		return staProName;
	}

	/**
	 * @param staProName
	 *            the staProName to set
	 */
	public void setStaProName(String staProName) {
		this.staProName = staProName;
	}

	/**
	 * @return the countryCode
	 */
	public String getCountryCode() {
		return countryCode;
	}

	/**
	 * @param countryCode
	 *            the countryCode to set
	 */
	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
}
